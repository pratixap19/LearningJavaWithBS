package Interface;

public class Notes {
	/*What is abstraction?
	 * Abstraction is a mechanism in Java to hide implimentation and showing only the functionality.
	 * We car achieve abstraction two ways.
	 * 1. Using interface
	 * 2. Using Abstract classes
	 * 
	 * points to remember while implementing Interface.
	 * 0. An interface is a blue print of a class.
	 * 1. It contains abstract methods.(It also allows default and static methods from Java8 onwards)
	 * 2. By default all variables are static and final.
	 * 3. We can't create object of an interface but we can create object reference.
	 * 4. By default all methods are public.
	 * 5. it is used to achieve multiple inheritance and abstraction
	 * 6. We can't create constructor in interface.
	 * 7. We can define interface with interface keyword.
	 * 8. A class extends another class, an interface extends another interface but a class implements an interface.
	 * 
	 * 
	 * Points to remember while implementing Abstract class
	 * 1. We can't create object. It can't be instantiated.
	 * 2. It can have abstract and non abstract methods
	 * 3. Abstract class must be declared with abstract keyword
	 * 4. It can have constructor and static methods.
	 * 5. It can have final methods which will force the subclass not to change the body of the method.
	 * 
	 */
	
}
