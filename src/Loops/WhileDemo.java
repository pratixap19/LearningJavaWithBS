package Loops;

public class WhileDemo {

	public static void main(String[] args) {
		//while loop is used to execute statement or block of statements repitadly when condition is true.
		//It is used when number of iterations are not known in advance.
		
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
	}

}
