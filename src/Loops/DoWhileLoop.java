package Loops;

public class DoWhileLoop {
	
	public static void main(String[] args) {
		//Notes
		//It executes statement or block of statements repeatedly when condition is true. 
		//In case of do while loop code will executes at least once irrespective of the condition.
		//loop 
		int i=11;
		do{
			System.out.println(i);
			i++;
		}while(i<=10);
		}
}
